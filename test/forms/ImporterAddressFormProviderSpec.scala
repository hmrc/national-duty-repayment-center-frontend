package forms

import forms.behaviours.StringFieldBehaviours
import play.api.data.FormError

class ImporterAddressFormProviderSpec extends StringFieldBehaviours {

  val requiredKey = "importerAddress.error.required"
  val lengthKey = "importerAddress.error.length"
  val maxLength = 9

  val form = new ImporterAddressFormProvider()()

  ".line1" must {

    val fieldName = "line1"
    val requiredKey = "address.error.line1.required"
    val lengthKey = "address.error.line1.length"
    val invalidKey = "address.error.line1.invalid"
    val maxLength = 35

    behave like fieldThatBindsValidData(
      form,
      fieldName,
      stringsWithMaxLength(maxLength)
    )

    behave like fieldWithMaxLength(
      form,
      fieldName,
      maxLength = maxLength,
      lengthError = FormError(fieldName, lengthKey, Seq(maxLength))
    )

    behave like mandatoryField(
      form,
      fieldName,
      requiredError = FormError(fieldName, requiredKey)
    )
  }
}
